attach(affairs)
colnames(affairs)
max(affairs)
affairs$affairs <- cut(affairs$affairs, breaks = c(-1, 0.5, 12), labels = 0:1)
logit = glm(affairs ~ X+age+yearsmarried+factor(children)+religiousness+education+occupation+rating, family = "binomial", data = affairs)
prob = predict(logit,type = c("response"),affairs)
prob
confusion <- table(prob > 0.5,affairs$affairs)
confusion
accuracy <- sum(diag(confusion))/sum(confusion)
accuracy
rocrpred <- prediction(prob,affairs$affairs)
rocrperf <- performance(rocrpred,'tpr','fpr')
plot(rocrperf,colorize=T,text.adj=c(-0.2,1.7)) # prefferable probability cut-off value = 0.46



















































































































































